int emu_soc_asic_init(struct amdgpu_device *adev)
{
	int r = 0;

	switch (adev->asic_type) {
	case CHIP_MERO:
		if (adev->pdev->device != 0x80) {
			if (adev->pdev->device != 0x81) { /* VEH LSD MODEL GFX 10.3 */
				if (adev->gmc.zfb_size > 0) {
					amdgpu_device_program_register_sequence(adev,
								mero_gopher_veh_df_lsd_init,
								(const u32)ARRAY_SIZE(mero_gopher_veh_df_lsd_init));
					amdgpu_device_program_register_sequence(adev,
								mero_gopher_veh_gc_lsd_init,
								(const u32)ARRAY_SIZE(mero_gopher_veh_gc_lsd_init));
					amdgpu_device_program_register_sequence(adev,
								mero_gopher_veh_vm_init,
								(const u32)ARRAY_SIZE(mero_gopher_veh_vm_init));
				} else {
					amdgpu_device_program_register_sequence(adev,
								mero_gopher_veh_df_lsd_lfb_init,
								(const u32)ARRAY_SIZE(mero_gopher_veh_df_lsd_lfb_init));
					amdgpu_device_program_register_sequence(adev,
								mero_gopher_veh_gc_lsd_lfb_init,
								(const u32)ARRAY_SIZE(mero_gopher_veh_gc_lsd_lfb_init));
					mero_gopher_veh_umc_lsd_lfb_init(adev);
					amdgpu_device_program_register_sequence(adev,
								mero_gopher_veh_vm_lsd_lfb_init,
								(const u32)ARRAY_SIZE(mero_gopher_veh_vm_lsd_lfb_init));
					amdgpu_device_program_register_sequence(adev,
								mero_gopher_veh_mmhub_lsd_lfb_init,
								(const u32)ARRAY_SIZE(mero_gopher_veh_mmhub_lsd_lfb_init));
				}
			} else { /* VEH LSC MODEL GFX 10.3 */
				amdgpu_device_program_register_sequence(adev,
							mero_gopher_veh_df_init,
							(const u32)ARRAY_SIZE(mero_gopher_veh_df_init));
				amdgpu_device_program_register_sequence(adev,
							mero_gopher_veh_gc_init,
							(const u32)ARRAY_SIZE(mero_gopher_veh_gc_init));
				mero_gopher_veh_umc_init(adev);
				amdgpu_device_program_register_sequence(adev,
							mero_gopher_veh_vm_init,
							(const u32)ARRAY_SIZE(mero_gopher_veh_vm_init));
			}
		}

		/* GOPHER LITE GFX 10.3 */
		amdgpu_device_program_register_sequence(adev,
					mero_gopher_lite_cl3951443_golden_init,
					(const u32)ARRAY_SIZE(mero_gopher_lite_cl3951443_golden_init));
		break;
	default:
		DRM_ERROR("emulation is not supported for unknown asic\n");
		r = -EINVAL;
		break;
	}

	return r;
}
